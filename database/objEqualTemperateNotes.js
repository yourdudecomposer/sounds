import notesNames from "./notesNames.js";

let arr = [];
const startFrequency = 8.175798915643707;

const octavesCount = 12;
const notesInOctave = 12;
const allNotesCount = octavesCount * notesInOctave + 1;

//fill array of frequency
for (let i = 0; i < allNotesCount; i++) {
  arr[i] = startFrequency * 2 ** (i / 12);
}

// let arrA = arr.splice(9).filter((el, ind) => ind % 12 == 0) get All A notes

//make an object with notes


let startOctaveNum = -1;
let indexOfNotesName = 0;
const objEqualTemperateNotes = arr.reduce((acc, freq) => {
  let objProp = notesNames[indexOfNotesName] + startOctaveNum;

  if (indexOfNotesName < notesNames.length-1) indexOfNotesName++;
  else {
    indexOfNotesName = 0;
    startOctaveNum++
  }

  return { ...acc, [objProp]: freq };
}, {});

//finally we get obj with all notes

export default objEqualTemperateNotes;

